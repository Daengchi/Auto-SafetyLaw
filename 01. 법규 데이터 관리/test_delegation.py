"""위임 참조 매핑 로직 검증"""
from src.processor import build_3stage_table, find_missing_articles

# 법률: 제5조, 제6조, 제7조
law = [
    {"번호": "5", "제목": "안전조치"},
    {"번호": "6", "제목": "보건조치"},
    {"번호": "7", "제목": "위원회"},
]

# 시행령: 제3조는 법 제5조 위임, 제4조는 법 제5조 및 제6조 위임, 제10조는 법 없음
enf = [
    {"번호": "3", "제목": "안전조치 세부사항", "내용": "법 제5조에 따라 사업주는 다음의 안전조치를 하여야 한다."},
    {"번호": "4", "제목": "보건조치 기준",     "내용": "법 제5조 및 법 제6조에 따른 보건조치 기준을 정한다."},
    {"번호": "10", "제목": "독립 시행령 조문",  "내용": "시행령 자체 규정으로 별도 내용."},
]

# 시행규칙: 제2조는 영 제3조 위임, 제5조는 영 없음
rul = [
    {"번호": "2", "제목": "안전조치 절차", "내용": "영 제3조에 따라 안전조치 절차를 정한다."},
    {"번호": "5", "제목": "독립 시행규칙",  "내용": "독립 규정 내용."},
]

df = build_3stage_table(law, enf, rul)
print("=== 3단비교표 ===")
print(df.to_string(index=False))
print()

# 검증: 법 제5조 → 시행령 제3조 → 시행규칙 제2조 연결이 있어야 함
row = df[(df["법률 조문번호"] == "제5조") & (df["시행령 조문번호"] == "제3조") & (df["시행규칙 조문번호"] == "제2조")]
assert len(row) == 1, "법 제5조 → 시행령 제3조 → 시행규칙 제2조 연결 실패"
print("위임 연결 검증: OK")

# 검증: 법 제5조 → 시행령 제4조 (시행규칙 없음) 도 있어야 함
row2 = df[(df["법률 조문번호"] == "제5조") & (df["시행령 조문번호"] == "제4조")]
assert len(row2) >= 1, "법 제5조 → 시행령 제4조 연결 실패"
print("복수 시행령 연결 검증: OK")

# 검증: 법 제7조는 시행령이 없으므로 시행령 빈칸
row3 = df[df["법률 조문번호"] == "제7조"]
assert row3.iloc[0]["시행령 조문번호"] == "", "법 제7조 시행령 빈칸이어야 함"
print("미연결 조문 빈칸 검증: OK")

# 갭 분석 검증
law_nums = {a["번호"] for a in law}
enf_nums = {a["번호"] for a in enf}
missing_enf = find_missing_articles(enf, law_nums)
missing_rul = find_missing_articles(rul, enf_nums)

assert "제10조" in missing_enf["조문번호"].values, "시행령 제10조가 누락에 없음"
assert "제5조" in missing_rul["조문번호"].values, "시행규칙 제5조가 누락에 없음"
print("갭 분석 검증: OK")

print("\n모든 검증 통과")
